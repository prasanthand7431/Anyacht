package com.anyacht.app.model;

public class BookingDetails {

    private String id;
    private String title;
    private String id_destination;
    private String checked;
    private String rank;
    private String max_children;
    private String max_adults;
    private String max_people;
    private String price;
    private String quantity;
    private String dest;
    private String file;
    private String descr;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public String getId_destination() {
        return id_destination;
    }

    public void setId_destination(String id_destination) {
        this.id_destination = id_destination;
    }

    public String getChecked() {
        return checked;
    }

    public void setChecked(String checked) {
        this.checked = checked;
    }

    public String getRank() {
        return rank;
    }

    public void setRank(String rank) {
        this.rank = rank;
    }

    public String getMax_children() {
        return max_children;
    }

    public void setMax_children(String max_children) {
        this.max_children = max_children;
    }

    public String getMax_adults() {
        return max_adults;
    }

    public void setMax_adults(String max_adults) {
        this.max_adults = max_adults;
    }

    public String getMax_people() {
        return max_people;
    }

    public void setMax_people(String max_people) {
        this.max_people = max_people;
    }

    public String getPrice() {
        return price;
    }

    public void setPrice(String price) {
        this.price = price;
    }

    public String getQuantity() {
        return quantity;
    }

    public void setQuantity(String quantity) {
        this.quantity = quantity;
    }

    public String getDest() {
        return dest;
    }

    public void setDest(String dest) {
        this.dest = dest;
    }

    public String getFile() {
        return file;
    }

    public void setFile(String file) {
        this.file = file;
    }

    public String getDescr() {
        return descr;
    }

    public void setDescr(String descr) {
        this.descr = descr;
    }
}
